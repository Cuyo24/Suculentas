<h1 align="Center"> Cactáceas: unas suculentas muy mexicanas </h1>

	<div>
		<p>Debido a lo accidentado de su espacio geográfico, la flora de México es considerada una de las más ricas y variadas del mundo; las grandes cadenas montañosas, su posición geográfica entre el norte y el sur del continente donde pasan el trópico de Cáncer y el trópico de Capricornio y la conjunción de las zonas Neártica y Neotropical  provoca un elevado índice de endemismo, pues numerosas entidades taxonómicas son autóctonas. </p>

		<p>La familia Cactaceae es una de las familias botánicas más diversas del reino Plantae, comprende aproximadamente 100 géneros y 1500 especies,  es endémica de América y se distribuye a lo largo del continente, desde Canadá a 59° de latitud Norte hasta la Patagonia en Argentina a 52° de latitud Sur; y desde el nivel del mar, en las dunas costeras, hasta los 5100 m de altitud en Perú.</p>	


	</div>



