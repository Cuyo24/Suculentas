	<div>
		<p>Así, las cactáceas se encuentran entre los elementos más característicos de la flora mexicana distribuyéndose primordialmente en matorrales xerófilos de las principales zonas áridas del país y, en menor medida, en los bosques tropicales caducifolios y espinosos, entre los que destacan la Cuenca del Balsas y el Istmo de Tehuantepec. En regiones cálido-húmedas son relativamente comunes algunas especies de cactáceas epífitas.</p>
		

