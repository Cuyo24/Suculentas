<h1 align="center">Al rededor del mundo</h1>
	<div>
		<p align="center">Hay miles de especies de suculentas, clasificadas en varias familias. La mayoría de estas plantas pertenecen a las <b>aizoáceas</b>,a las <b>Agavoideae</b> y a las <b>Didiereaceae</b>.</p>
	</div>

	<div>
		<h1>Aizoáceas</h1>
		<p align="center"><b>Hábitat:</b> principalmente en el sur de África, aunque se las encuentra en Madagascar, cuerno de África, Arabia, Isla de Elba, Australia, Nueva Zelanda.</p> 
		
		<p align="center"><b>Descripción:</b> la familia del las Aizoaceae (Rudolphi) comprende unos 130 géneros y algunos miles de especies. Se divide en cinco subfamilias: Ruschioideae (con un centenar de géneros, en su mayoría ex Mesembriantemaceae), Mesembryanthemoideae, Aizooideae, Sesuvioideae, Tetragonioideae. Se trata de plantas ornamentales, dicotiledóneas, con crecimiento durante el período de floración. Se encuentran tanto a nivel del mar como a 3.000 m. de altitud. Las hojas son suculentas y de aspecto variado. Las flores, vistosas y duraderas, son actinomorfas, hermafroditas, de tipo tetrámero o pentámero, con cáliz de 5 sépalos y corola con muchos pétalos. Androceo con muchos estambres y gineceo en general súpero, con 3- carpelos.  El fruto es una cápsula dehiscente que se abre al ser mojada.</p>

