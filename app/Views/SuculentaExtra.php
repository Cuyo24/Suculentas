<div>
		<h1>Didiereaceae</h1>
		<p align="center"><b>Hábitat:</b>Madagascar</p> 
		
		<p align="center"><b>Descripción:</b> Didiereaceae es una familia de plantas suculentas nativa de Madagascar y África. A día de hoy se incluyen en su clasificación solo 6 géneros y 20 especies, convirtiéndola en una de las familias más pequeñas.
		Las plantas de esta familia son propias de hábitats áridos y están adaptadas a la vida con muy poca agua, por lo que sus cuidados son muy sencillos.
		Aunque no es especialmente popular, muchos amantes de las plantas suculentas encuentran en esta familia la forma de dar un toque original a su jardín gracias a su curioso aspecto espinoso y alargado.</p>

