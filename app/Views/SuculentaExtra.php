<div>
		<h1>Agavoideae</h1>
		<p align="center"><b>Hábitat:</b> principalmente en el América Central y del Norte </p> 
		
		<p align="center"><b>Descripción:</b> Usualmente hierbas grandes arrosetadas, o árboles, o arbustos, o subarbustos. Rizomatosas, algunas con tallos con crecimiento secundario anómalo. Presentan cristales de oxalato de calcio de tipo rafidio y saponinas esteroideas. Pelos simples. Son acaulescentes (carecen de tallo salvo el que da las flores y frutos), o poseen rizoma, o bulbo, o son arborescentes. En los taxones con tallos, son ramificados simpodiales.
		Hojas muchas veces grandes, xeromórficas, fibrosas o raramente suculentas, alternas y espirales, en rosetas en la base o el final de las ramas, simples, de margen entero a espinoso-serrado, y usualmente con una espina aguda en el ápice, con venación paralela, los haces vasculares asociados con fibras gruesas y fuertes, envainadoras en la base, sin estípulas.</p>

