<h1 align="Center">Decoraciones "Suculentas"</h1>

<p align="Center">Decorar tu casa y jardín con suculentas y cactus es una alternativa original y de gran personalidad. Hay muchas maneras de decorar con este tipo de plantas el interior y exterior de tu hogar.</p>

<div>
	<h1>Cactus y suculentas para decorar tu casa: los cactus en la decoración</h1>

	<p>
		Los cactus y las suculentas pertenecen a la familia de las cactáceas, pero las distingue un factor importante: el lugar en el que han crecido. Esta ubicación les otorga características diferentes debido a las condiciones del clima lo que las hace evolucionar de forma diferente, una de las adaptaciones más evidentes es su capacidad de retener el agua. Ambas especies ofrecen una extensa variedad de plantas con formas llamativas y particulares, lo que las hace tan atractivas para decorar los jardines y los hogares. </p>

		<p>Los cactus son característicos de lugares desérticos, que han modificado la presencia de las hojas por espinas, lo que les ayuda a contar con reservas de agua, la cual se aloja en el tronco de la planta. Hay alrededor de 73 especies de cactus, que se manifiestan en los lugares en los que se encuentran, por lo que se pueden observar ejemplares de diversos colores, formas, incluso algunos cuentan con flores.</p>



