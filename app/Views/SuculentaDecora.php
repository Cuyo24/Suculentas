	<h1>Decorar con suculentas: jardines verticales</h1>

	<p>
		Los jardines verticales siempre son espectaculares, pero con suculentas se consigue un efecto más impactante ya que podemos jugar con sus formas y colores sin necesitar grandes espacios. Es como tener un montón de flores carnosas formando un cuadro vivo en la pared. </p>

