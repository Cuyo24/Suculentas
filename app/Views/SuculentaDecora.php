	<h1>Decorar con suculentas: botellas</h1>

	<p>
		Una idea que nos ha enamorado es la de reciclar una botella de vino o de licor y adaptarla para utilizarla como maceta de suculentas.  Queda preciosa como centro de mesa o para regalar a esa persona que es incapaz de mantener una planta viva. ¡Regala suculentas! Son la solución para los amantes de las plantas a quienes siempre se les mueren.
