	<h1>Decorar con suculentas: terrarios</h1>

	<p>
		Un bonito terrario es muy fácil de crear: te basta con una pecera vacía o un recipiente mono de cristal, como muestran todas estas fotos. Puedes colgarlo de la pared o colocarlo encima de una mesa; sean del estilo que sean, los terrarios de suculentas son una maravilla </p>

