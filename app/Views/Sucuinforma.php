<h1 align="center">¿Qué sabes sobre las soculentas?</h1>

<div align="center">

<h1 align="left">Las soculentas son:</h1>

			<blockquote >
			<p>Las plantas suculentas —del latín suculentus, 'jugoso'— o 
			crasas son aquellas en las que algún órgano está especializado en el almacenamiento de 
			agua en cantidades mayores que las plantas sin esta adaptación.</p>
				
				
			<p>Estos órganos de reserva tienen una alta proporción de tejido parenquimático.Su adaptación les permite mantener reservas de agua durante períodos prolongados, y sobreviven a los largos períodos de sequía en climas áridos</p>
			</blockquote >		
				
				
</div>

