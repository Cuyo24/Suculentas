<div align="left">

<h1 align="left">Son guerreras de la naturaleza</h1>

<blockquote >
		<p>La adaptación de las suculentas les permite colonizar entornos áridos o en los que la captación de agua es limitada, aquellos que reciben poca competencia por parte de otras especies vegetales y en los que los herbívoros son escasos.</p> 

		<p>Para posibilitar la captación de la escasa humedad presente en el ambiente, muchas suculentas son pubescentes, es decir, presentan una superficie cubierta de pelillos que retienen el rocío matutino.</p>
</blockquote >

</div>

